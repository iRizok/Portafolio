






/*
OBTENER LA LISTA DE EMPLEADOS QUE INGRESARON ENTRE 01/05/1979 AL 25/10/1984
*/
SELECT * FROM EMPLEADO 
WHERE FECHAALTA BETWEEN TO_DATE('01/05/1979', 'DD/MM/YYYY') AND TO_DATE('25/10/1984', 'DD/MM/YYYY');

/*
OBTENER LA LISTA DE LOS EMPLEADOS QUE NACIERON ENTRE 1960 Y 1990
*/
SELECT * FROM EMPLEADO WHERE SUBSTR(RFC, 5, 2) BETWEEN '60' AND '90';

